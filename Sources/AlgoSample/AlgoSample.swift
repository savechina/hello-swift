import Foundation
import Algorithms

/// simple add ,calc two number sum
/// 计算二个数之和
/// - Parameters:
///   - x: one x int
///   - y: two y int
/// - Returns: sum , x and y
public func AddTwo(x: Int, y: Int) -> Int {
    //计算两个数的和
    var z = x + y

    return z
}

/// 计算三个数之和
///
///
/// - Parameters:
///   - x: x one
///   - y: y two
///   - z: z three
/// - Returns: sum 计算结果
public func AddThree(x: Int, y: Int, z: Int) -> Int {
    //计算三个数的和
    var n = x + y + z
    
    return n
}
